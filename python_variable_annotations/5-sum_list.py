#!/usr/bin/env python3
""" Module makes the sum of a list"""


def sum_list(input_list: [float]) -> float:
    """
    Computes the sum of a list of floats.

    Args:
        input_list (List[Float]): A list of floats.

    Returns:
        float: The sum of the floats in the list.
    """
    return sum(input_list)
